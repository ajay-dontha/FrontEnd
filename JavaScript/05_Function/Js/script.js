//functions without Parameters:

function displayNames() {
    console.log("Hello World");
}
displayNames();//function call

//with parameters

function June(name) {
    console.log(name);
}
June("Allari Pillodu");

//two parameters passing with functions

function fullName(name1,name2){
    console.log(name1,name2);
}
fullName("Shiva","Shankar")

//three parameter

function students(R1,R2,R3){
    console.log(R1,R2,R3);
    console.log(R2);
}
students("Sahithi","Aditya","AnuPriya");
