/**
 * Three diff types of alerts:
 * 
 * Alert
 * Prompt
 * Confirm
 */

//Alert
alert("Alert: Welcome to JavaScript"); //display message

//Confirm
confirm("Confirm: Do you want to continue");//Ok or cancel

//Prompt
prompt("Enter Values:")

var value=prompt("Enter Value:");
console.log(value);

//adding two numbers using prompt
var value1=Number(prompt("Enter value1"));
var value2=Number(prompt("Enter Value2"));
var sum=(value1+value2);
console.log(sum);

//Multiplication
var mul1=Number(prompt("Enter value1"));
var mul2=Number(prompt("Enter Value2"));
var mul=(mul1*mul2);
console.log(mul);

